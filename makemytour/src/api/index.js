export const cancelbooking = async (userId, bookingId, reason) => {
  try {
    const url = `${BACKEND_URL}/booking/cancel?userId=${userId}&bookingId=${bookingId}&reason=${encodeURIComponent(reason)}`;
    const res = await axios.post(url);
    return res.data;
  } catch (error) {
    console.log(error);
    throw error;
  }
};
